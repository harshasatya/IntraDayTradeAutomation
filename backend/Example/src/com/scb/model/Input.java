package com.scb.model;

public class Input {

	int no_of_shares;
	int profit_per;
	int loss_per;
	int time;
	public int getNo_of_shares() {
		return no_of_shares;
	}
	public void setNo_of_shares(int no_of_shares) {
		this.no_of_shares = no_of_shares;
	}
	public int getProfit_per() {
		return profit_per;
	}
	public void setProfit_per(int profit_per) {
		this.profit_per = profit_per;
	}
	public int getLoss_per() {
		return loss_per;
	}
	public void setLoss_per(int loss_per) {
		this.loss_per = loss_per;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
}